	ORG	0000H

	LJMP	MAIN
LENGTH	EQU	60H
INDEX	EQU	20H

MAIN:
	ACALL	LOAD_WORD
	MOV	R0, #30H
	MOV	A, @R0
	MOV	LENGTH, A
	INC	R0
	MOV	DPTR, #RRESHTI
	MOV	INDEX, #00H
LOOP:
	CLR	A
	MOVC	A, @A+DPTR
	INC	DPTR
	INC	INDEX
	MOV	P2, INDEX
	MOV	40H, A
	CJNE	A, #0DH, SKIP
	INC	DPTR
	MOVC	A, @A+DPTR
	CJNE	A, #0AH, ENDD
SKIP:
	MOV	A, @R0
	CJNE	A, #0, NOT_FOUND
	MOV	A, INDEX
	SUBB	A, LENGTH
	MOV	P1, A
	SJMP	TEND
N_EQU:
	MOV	R0, #30H
	INC	R0
	SJMP	LOOP

ENDD:
	MOV	P1, #0
	SJMP	TEND
NOT_FOUND:

	CJNE	A, 40H, N_EQU
	INC	R0
	SJMP	LOOP

FJALA:	DB	3, 'WORD', 00H
RRESHTI:	DB	' THIS IS THE WORD WE ARE LOOKING FOR', 0DH, 0AH
TEND:	END

