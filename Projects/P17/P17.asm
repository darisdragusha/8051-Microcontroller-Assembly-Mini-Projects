; P1.0 AND P1.1 = SWITCHES THAT DETERMINE IN WHICH STATE THE MC IS. THE STATE IS SHOWN IN THE 7SEGMENT CA DISPLAY CONNECTED TO PORT 3

	ORG	0000H
	LJMP	MAIN

MAIN:
	MOV	A, P1
	ANL	A, #03H
	ACALL	DISPLAY_STATE
	SJMP	MAIN

DISPLAY_STATE:
	MOV	DPTR, #SEG
	CLR	A
	MOVC	A, @A+DPTR
	MOV	P3, A
	RET


SEG:	DB	0x3F, 0x06, 0x5B, 0x4F, 0x66, 0x6D, 0x7D, 0x07, 0x7F, 0x6F	; 0-9
	DB	0x77, 0x7C, 0x58, 0x5E, 0x79, 0x71	; A-F

