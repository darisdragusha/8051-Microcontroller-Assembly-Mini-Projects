; BLINK LEDS IN P1 WITH A T_PERIOD = 500ms AND T_OFF = 200ms
	ORG	0000H

MAIN:
	MOV	P1, #0
	MOV	A, #2		; 200ms DELAY
	ACALL	DELAY
	MOV	P1, #0FFH
	MOV	A, #3		;300ms DEALY
	ACALL	DELAY
	SJMP	MAIN

DELAY:
	MOV	R0, #10
	MOV	B, R0
	MUL	AB
LOOP1:	MOV	R1, #99
LOOP2:	MOV	R2, #50
	DJNZ	R2, $
	DJNZ	R1, LOOP2
	DJNZ	A, LOOP1
	MOV	R7, #255
	DJNZ	R7, $
	MOV	R7, #255
	DJNZ	R7, $
	RET
	END
