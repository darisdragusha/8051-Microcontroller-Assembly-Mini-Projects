; TAKE THE INPUT FROM P1, TRANSFORM IT TO 7SEGMENT CA  AND SERIALLY TRANSMIT IT TO PIN P3.1 CONNECTED TO THE 4094IC

	ORG	0000H
	LJMP	MAIN

STR	EQU	P3.2
D	EQU	P3.1
CLK	EQU	P3.0

MAIN:
	MOV	A, P1
	ACALL	BIN_TO_7SEG_CA
	ACALL	TRANSMIT
    SJMP MAIN

BIN_TO_7SEG_CA:
	ANL	A, #0FH
	MOV	DPTR, #SEG
	MOVC	A, @A+DPTR
	RET

TRANSMIT:
	MOV	R0, #8
	CLR	C
ALL_BITS:
	RLC	A
	MOV	P3.4, C
	SETB	CLK
	CLR	CLK
	DJNZ	R0, ALL_BITS
	SETB	STR
	CLR	STR
	RET


SEG:	DB	0x3F, 0x06, 0x5B, 0x4F, 0x66, 0x6D, 0x7D, 0x07, 0x7F, 0x6F	; 0-9
	DB	0x77, 0x7C, 0x58, 0x5E, 0x79, 0x71	; A-F
	DB	0xFF, 0xBF	; CLEAR DISPLAY AND '-'

	END
